function soma(n1,n2){
    return n1 + n2
}
function subtracao(n1,n2){
    return n1 - n2
}
function mult(n1,n2){
    return n1 * n2
}
function div(n1,n2){
    return n1 / n2
}
function potencia(n1,n2){
    return n1 * n1
}
function raiz(n1){
    return Math.sqrt(n1)
}

module.exports  = {
    soma,
    subtracao,
    mult,
    div,
    potencia,
    raiz,
}








